                            <p>Callan Cohen, who is the director of Birding Africa, has written two highly acclaimed books on where to watch birds in the region, including the best-selling <a href='https://www.birdingafrica.com/resources/sa_birdfinder.htm' target='_blank'>Southern African Birdfinder</a>. 
                                Callan divides his guiding time between his home turf in southern Africa and leading trips to most sub-Saharan countries, but focussed on South Africa, <a href='https://www.birdingafrica.com/africa_tours/birdingtournamibia.htm' target='_blank'>Namibia</a>, 
                                <a href='https://www.birdingafrica.com/africa_tours/birdingtouruganda.htm' target='_blank'>Uganda</a>, <a href='https://www.birdingafrica.com/africa_tours/birdingtourcameroon.htm' target='_blank'>Cameroon</a>, <a href='https://www.birdingafrica.com/africa_tours/birdingtourmadagascar.htm' target='_blank'>Madagascar</a>, 
                                <a href='https://www.birdingafrica.com/africa_tours/birdingtourgabon.htm' target='_blank'>Gabon</a> and <a href='https://www.birdingafrica.com/africa_tours/birdingtourethiopia.htm' target='_blank'>Ethiopia</a>.</p>
                            <p>Below, we have listed some options and ideas for 1 or 2 day trips from Cape Town. 
                               If you will be based in Cape Town, these would allow you to cover a high proportion of the endemics and specials, and you will also get to see an excellent diversity of habitats and landscapes.</p>
                            <h3 class='widgettitle'>Some Short Trips Around Cape Town</h3>
                            <ol>
                                <li><p>A day on the Cape Peninsula and possibly adjacent Hottentots' Holland mountains, for fynbos and coastal endemics including Hottentot Buttonquail, Knysna Warbler and Cape Siskin and some water birding at the Strandfontein Sewage Works.</p></li>
                                <li><p>A day on the West Coast, concentrating on the Langebaan area found north of Cape Town, for very diverse strandveld (coastal scrub and thicket) and coastal birding and shorebirds.</p></li>
                                <li><p>If you would be willing to spend a night or two out of Cape Town, a two-day loop to the Tanqua Karoo semi-desert which can be found north-east of Cape Town is an excellent option.</p> 
                                    <p>It is also perfectly possible to tackle the Karoo in a day trip out of Cape Town but two days is preferable as you may get the chance to take in some good mountain birding, notably for Protea Seedeater along the way, and the chance to spend a night at a great spot on the escarpment above the Karoo. 
                                       The Tanqua Karoo is a superb birding area offering the majority of the Karoo desert endemics, including Cinnamon-breasted Warbler and Karoo Eremomela in wonderful landscapes.</p> 
                                    <p>We usually stay overnight at a very picturesque private nature reserve on the escarpment above the desert which offers, in addition to Karoo cuisine and Bushman rock art, a good chance to spot the Cape Eagle Owl on a night drive.</p></li>
                                <li><p>A two day loop to the Overberg (South Coast) region east of Cape Town, overnighting at a guest farm adjacent to Grootvadersbosch, the largest indigenous forest in the south-western Cape. 
                                       The forest offers access to a number of traditionally more easterly species for example, Narina Trogon, Crowned Eagle and Grey Cuckooshrike.</p> 
                                    <p>The Overberg has a number of endemic species of grassland and thicket including Blue Crane, Denham's Bustard, Cape Vulture, Southern Tchagra and the highly localised Agulhas Long-billed Lark.</p></li>
