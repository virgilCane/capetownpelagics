                        <p>The view of Cape Point from beneath the towering cliffs, looking up from the sea level is simply breathtaking. 
                          In the right conditions, you can have truly fabulous opportunities to capture albatrosses and petrels in flight as they cruise past little more than an arms-reach from you, or swarm in their thousands behind an active trawler. 
                          Whether you just want to capture some images to remind you of your fabulous trip, or you are a serious amature or professional photographer, a camera on a Cape Town Pelagics trip is a must! </p>
