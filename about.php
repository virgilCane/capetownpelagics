                                        <p>Callan has had a life-long dedication to birds and founded Birding Africa when he was still a university student. 
                                           Since then, he has led over 100 tours and expeditions to 23 African countries, both for Birding Africa and British and American bird tour companies. 
                                           Callan has acted as a consultant for the BBC Natural History Unit and has even shown Bill Oddie some of the birds around Cape Town! </p>

                                        <p>Callan has spent much of his life traveling to the remotest parts of Africa in search of birds, with his highlights being finding Congo Peafowl after 17 days on foot and canoe in Africa's largest rainforest, Warsangli Linnets in the Daalloo mountains of Somalia, and rediscovering Namuli Apalis in Mozambique, not seen since it was described to science in 1932. 
                                        He has co-authored two birding books, including the <a href='http://www.capetownpelagics.com/resources/sa_birdfinder.htm' target='_blank'>Southern African Birdfinder</a>, a guide to finding over 1400 species in the southern third of Africa and Madagascar (and was once the youngest person to have seen a landmark 800 species in southern Africa, a few years ago now!). 
                                        Callan has served on the Birdlife South Africa Council as Chairman of the Cape Bird Club, and chaired the Western Cape Rarities Committee. </p>

