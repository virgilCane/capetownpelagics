                                        <p>Andrew comes from a family passionate about nature and the outdoors, and was brought up with a bird book at his side.</p>
                                        <p>What was a passing interest in childhood has grown into an academic career trajectory, with Andrew having completed his undergraduate studies at the University of Cape Town (UCT) in 2013, majoring in Applied Biology and Ecology & Evolution. 
                                            Andrew's Honours year saw him working on Spotted Hyenas in northern KwaZulu-Natal, before leading a research project for 4 months investigating the importance of birding tourism to national parks around South Africa.</p> 
                                        <p>In 2015 Andrew began his Masters degree through the Percy FitzPatrick Institute of African Ornithology at UCT, looking at the disturbance effects of boat-based tourism on the waterbirds of the Ramsar-designated De Hoop Vlei. 
                                            While undergoing fieldwork Andrew has recorded in excess of 200 birds on the reserve, one of the most complete bird lists available for the area. </p> 
                                        <p>Andrew is passionate about sharing South Africa's biodiversity with everyone he meets, and is currently studying part-time towards his FGASA Level 1 Nature Guide course. His ultimate bird moment was assisting with the fitment of a GPS-tracking unit to a majestic Martial Eagle in Kruger National Park.</p> 
                                        <p> Andrew founded the UCT Birding Club along with Birding Africa guides Seth Musker and Campbell Fleming, and was elected chairperson for its inaugural year in 2016. </p> 
