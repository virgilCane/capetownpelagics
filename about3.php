                                        <p><em>We're still in shock following the tragic death of our friend Barrie Rose on 30 December 2016. The southern oceans have lost one of their most knowledgeable conservationists, and his incredible sharing of knowledge will be missed. 
                                            Barrie died after slipping from the sea cliffs near Cape Point while angling with some of his closest friends. A more detailed tribute is linked <a href='https://www.sadstia.co.za/news/tribute-to-barrie-rose/' target='_blank'>here</a>. </em></p>

                                        <p>Barrie is famous as the most experienced pelagic birder in Cape Town who has added tremendously to our knowledge of rare seabirds off South Africa</p> 
                                        <p>Born and bred Capetonian, Barrie's deep interest in nature started with his grandfather Walter Rose, a pioneering herpetologist after whom the endemic Table Mountain Ghost Frog (Heleophryne rosei) is named. 
                                            Land birds and pelagic seabirds, the region's mammals, reptiles and frogs absorbed Barrie's fascination and led him to explore unsual places in Africa (Equatorial Guinea, Socotra and sub-Antarctic Islands), South America, Asia, the Antipodes and Antarctica. </p>
                                        <p>Barrie's observations of dwindling seabird populations, while he worked at Marine and Coastal Management and I & J Fishing, led him to institute changes that reduce the impacts of fisheries on seabirds.</p>       
                                        <p>He consulted for the seabird portion of Sasol Birds of Southern Africa, the 'Seasonal Table for Seabirds' in <a href='https://amzn.to/2MOkYRS' target='_blank'>Essential Birding</a> and the <a href='https://amzn.to/37Ad7zh' target='_blank'>Southern African Birdfinder</a>, and the South African Rareties Committee for over ten years. 
                                            Barry was a talented photographer (but a birder first and foremost) and guided for local and international birding companies for over 30 years.</p>     
                                        <p>Barrie monitored conservation compliance in international fishing vessels and regularly led trips for Cape Town Pelagics and Birding Africa. </p>                           
